function add() {
    var name1 = parseInt(document.getElementById("name1").value);
    var name2 = parseInt(document.getElementById("name2").value);

    var result = name1 + name2;
    alert(result);
}



function sub() {
    var name1 = parseInt(document.getElementById("name1").value);
    var name2 = parseInt(document.getElementById("name2").value);

    var result = name1 - name2;
    alert(result);
}


function mul() {
    var name1 = parseInt(document.getElementById("name1").value);
    var name2 = parseInt(document.getElementById("name2").value);

    var result = name1 * name2;
    alert(result);
}

